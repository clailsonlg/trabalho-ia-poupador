package algoritmo;

public class ValorMatriz {

	int passada = 0;
	int v = 0;
	
	public void setValores(int passada, int v) {
		
		this.passada = passada;
		this.v = v;
		
	}

	public int getPassada() {
		return passada;
	}

	public void setPassada(int passada) {
		this.passada = passada;
	}

	public int getV() {
		return v;
	}

	public void setV(int v) {
		this.v = v;
	}
	
}
